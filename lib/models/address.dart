class Address {
  String firstName;
  String lastName;
  String city;
  String address;
  String addressMap;
  String mobileNumber;
  bool isDefault;

  Address({
    required this.firstName,
    required this.lastName,
    required this.city,
    required this.address,
    required this.addressMap,
    required this.mobileNumber,
    required this.isDefault,
  });
}
